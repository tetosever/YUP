package app.YUP.Enum;

/**
 * Enum representing different types of event tags.
 * This enum is used to categorize events into different types or categories.
 * Each constant in this enum represents a different type of event.
 * Currently, there are three types of events: PARTY, LIVEMUSIC, and APERITIVO.
 */
public enum EventTag {
    /**
     * Represents a party event.
     * This type of event typically involves a social gathering of invited guests, typically involving eating, drinking, and entertainment.
     */
    PARTY,

    /**
     * Represents a live music event.
     * This type of event typically involves a performance of music in front of an audience by live musicians.
     */
    LIVEMUSIC,

    /**
     * Represents an aperitivo event.
     * This type of event typically involves a pre-meal drink specifically meant to whet your appetite.
     */
    APERITIVO
}