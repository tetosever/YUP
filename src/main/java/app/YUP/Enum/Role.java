package app.YUP.Enum;

/**
 * Enum representing different user roles in the application.
 * This enum is used to define the roles that a user can have in the application.
 * Each constant in this enum represents a different user role.
 * Currently, there is one role: LOGGED_USER, which represents a user that is logged in.
 */
public enum Role {

    /**
     * Represents a user that is logged in.
     * This role is assigned to users after they successfully authenticate.
     */
    LOGGED_USER
}